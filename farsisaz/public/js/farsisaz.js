function setDatePicker(frm,data_field_name,engField,perField,withTime){
    perField_$input=$("input[data-fieldname='"+data_field_name+"']" ).first();
    var setting;
    if(withTime){
        setting = {format: 'YYYY-MM-DD HH:mm:ss',initialValue:false,initialValueType: 'persian',autoClose: true,calendar:{persian: {locale: 'fa'}},toolbox:{calendarSwitch:{enabled: false}}
        ,timePicker: {enabled: true}
        ,onSelect: function (unix) {
            engField.set_value(frappe.datetime.get_datetime_as_string(to.getState().selected.dateObject.State.gDate));
            }
        }
    }else{
        setting = {format: 'YYYY-MM-DD',initialValue:false, initialValueType: 'persian',autoClose: true,calendar:{persian: {locale: 'fa'}},toolbox:{calendarSwitch:{enabled: false}}
        ,onSelect: function (unix) {
            engField.set_value(frappe.datetime.obj_to_str(to.getState().selected.dateObject.State.gDate));
            }
        }
    }
        var to = perField_$input.persianDatepicker(setting);
        if(engField.get_value()){
            to.setDate(new Date(engField.datepicker.selectedDates).getTime());
        }
        

        frappe.ui.form.on(frm, engField.df.fieldname, function(frm) {
                to.setDate(new Date(engField.datepicker.selectedDates).getTime());
            }); 
}